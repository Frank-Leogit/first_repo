def somma_due_numeri(a: float=2, b: float = 5):
    '''questa funzione somma due float e restituisce
    il loro risultato sommato'''
    somma = a + b
    return somma
def area_triangolo(b:float=3,h:float=3):
    '''questa funzione calcola l'area del triangolo'''
    return (b*h/2)
def perimetro_triangolo(a:float=3,b:float=3,c:float=3):
    '''questa funzione calcola l'area del triangolo'''
    return (a+b+c)
def area_quadrato(a:float=3):
    '''questa funzione calcola l'area del quadrato'''
    return (a**2)
def perimetro_quadrato(a:float=3):
    '''questa funzione calcola l'area del quadrato'''
    return (a*4)
def cubo(a:float):
    return a**3,a**2*6
